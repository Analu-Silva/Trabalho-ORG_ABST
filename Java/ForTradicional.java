public class ForTradicional {
    public static void main(String[] args) {
        for (int i = 1; i <= 100; i++) {         // Primeira parte é a variavel de controle. A segunda é até aonde vai ir o loop. 
            System.out.println("Raiz de " + i + " é: " + Math.sqrt(i));  // O terceiro, é a modificação que vai ter nesse controle
        }
    }
}
